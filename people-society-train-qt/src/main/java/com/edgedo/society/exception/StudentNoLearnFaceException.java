package com.edgedo.society.exception;

import com.edgedo.common.base.BusinessException;

/**
 * @author WangZhen
 * @description
 * @date 2020/5/4
 */
public class StudentNoLearnFaceException extends BusinessException {

    public StudentNoLearnFaceException(String errmsg, String errCode) {
        super(errmsg, errCode);
        this.setErrCode("noLearnFace");
    }

    public StudentNoLearnFaceException() {
        super();
        this.setErrCode("noLearnFace");
    }

    public StudentNoLearnFaceException(String errmsg) {
        super(errmsg);
        this.setErrCode("noLearnFace");
    }

    public StudentNoLearnFaceException(String message, Throwable cause) {
        super(message, cause);
        this.setErrCode("noLearnFace");
    }

    public StudentNoLearnFaceException(Throwable cause) {
        super(cause);
        this.setErrCode("noLearnFace");
    }
}
